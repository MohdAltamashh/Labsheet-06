#Q10-  Write a program to iterate through a set and print its elements.


# 1. Create the initial set
countries = {"USA", "Canada", "Mexico", "Brazil", "UK", "Germany", "France", "Spain"}
print(f"Set of countries: {countries}")

# 2. Iterate through the set using a for loop
print("\n--- Iterating through the set ---")
for country in countries:
    # Print each element found during iteration
    print(f"Found country: {country}")